package pojoClassForSerilizationDeserilization;

public class Spouse {

	//declaration
		String wifeName;
		String BikeName;
		//constructor
		public Spouse(String wifeName, String bikeName) {
			super();
			this.wifeName = wifeName;
			BikeName = bikeName;
		}
		//getters and setters
		public String getWifeName() {
			return wifeName;
		}
		public void setWifeName(String wifeName) {
			this.wifeName = wifeName;
		}
		public String getBikeName() {
			return BikeName;
		}
		public void setBikeName(String bikeName) {
			BikeName = bikeName;
		}
		
	
		
	
	
}
